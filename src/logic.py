def create_folder(name):
    if os.path.exists(name) is False:
        os.makedirs(name)


def get_input_file_list():
    file_list = list()
    for root, dirs, files in os.walk("input"):
        for file in files:
            file_list.append(root + '\\' + file)
    return file_list


def input_to_output(path: str):
    return path.replace("input", "output", 1)


def get_file_name(path: str):
    return path[path.rfind("\\") + 1:]


def get_directory(path: str):
    return path[:path.rfind('\\')]


def generate_language_list(depth, end_language):
    available_languages = list(LANGUAGES.keys())
    language_list = list()
    for _ in range(depth):
        language = available_languages[randint(0, len(available_languages) - 1)]
        language_list.append(language)
    language_list.append(end_language)
    return language_list


def generate_log(output_file, file_name, language_list):
    output_file.write(file_name + "\n")
    for language in language_list:
        output_file.write("  " + LANGUAGES[language] + "\n")
    output_file.write("\n\n")
